function parseChapter(value?: string) {
  if (!value) return undefined;
  const parsed = Number.parseInt(value, 10);
  return Number.isFinite(parsed) ? parsed : undefined;
}

function parseHighlight(value?: string): HighlightRange | undefined {
  const normalized = value?.trim();
  if (!normalized) {
    return undefined;
  }

  const match = normalized.match(/^(\d+)(?:-(\d+))?$/);
  if (!match) {
    return undefined;
  }

  const startVerse = Number(match[1]);
  const endVerse = Number(match[2] ?? match[1]);
  if (!Number.isInteger(startVerse) || !Number.isInteger(endVerse) || startVerse < 1 || endVerse < startVerse) {
    return undefined;
  }

  return { startVerse, endVerse };
}

function parseSourceTag(value?: string) {
  const normalized = value?.trim().toLowerCase();
  if (!normalized || !/^[a-z0-9-]{1,32}$/.test(normalized)) {
    return undefined;
  }

  return normalized;
}

function verseIsHighlighted(highlight: HighlightRange | undefined, verseNumber: number) {
  return Boolean(highlight && verseNumber >= highlight.startVerse && verseNumber <= highlight.endVerse);
}

export async function generateMetadata({ params }: Props): Promise<Metadata> {
  const { locale: requestedLocale } = await params;
  const locale = await resolveLocale(requestedLocale);
  const copy = COPY[locale];

  return buildPageMetadata(locale, copy.title, copy.description, "/bible");
}

