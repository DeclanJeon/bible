# ── Build the QA JSON ──
def build_qa():
    # Count total questions
    total = sum(len(qs) for _, qs in CATEGORIES)
    print(f"Total questions: {total}")

    # Generate enough unique verses (4 per question)
    verses_per_q = 4
    pool = generate_unique_verses(total * verses_per_q)
    print(f"Verse pool size: {len(pool)}")

    # Assign verses to questions
    questions = []
    used = set()
    idx = 0

    for cat_name, cat_questions in CATEGORIES:
        for qid, prompt, terms in cat_questions:
            refs = []
            for _ in range(verses_per_q):
                while idx < len(pool) and pool[idx] in used:
                    idx += 1
                if idx < len(pool):
                    refs.append(pool[idx])
                    used.add(pool[idx])
                    idx += 1

            questions.append({
                "id": qid,
                "prompt": prompt,
                "expectedPrimaryRefs": refs,
                "expectedTerms": terms,
            })

    # Verify no duplicate verses
    all_refs = []
    for q in questions:
        all_refs.extend(q["expectedPrimaryRefs"])

    ref_counts = {}
    for r in all_refs:
        ref_counts[r] = ref_counts.get(r, 0) + 1

    dupes = {k: v for k, v in ref_counts.items() if v > 1}
    if dupes:
        print(f"WARNING: {len(dupes)} duplicate verses found!")
        for k, v in list(dupes.items())[:10]:
            print(f"  {k}: {v} times")
    else:
        print("✓ No duplicate verses!")

    print(f"Total unique verses used: {len(set(all_refs))}")
    return questions

